class Solution {
    public double myPow(double x, int n) {
        long N = n;
        return N > 0 ? quickMul(x, N) : 1 / quickMul(x, -N);
    }

    public double quickMul(double x, long N) {
        if (N == 0) {
            return 1.0;
        }

        double y = quickMul(x, N / 2);
        System.out.println(y);
        double ans = N % 2 == 0 ? y * y : y * y * x;
        return ans;
    }
}
